entrada_usurio = int (input('Digite um numero inteiro: '))
dobro = entrada_usurio * 2
triplo = entrada_usurio * 3
raiz_quadrada = entrada_usurio ** 2

print('O numero digitado foi {} o dobro dele é {} o triplo dele será {} e a raiz quadrada é {} '.format(entrada_usurio, dobro, triplo, raiz_quadrada))